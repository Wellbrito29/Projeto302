
public class Item {
	private String item;
	private float conserv;
	private boolean servico;
	private boolean disponivel;
	private String datalibera;
	
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public float getConserv() {
		return conserv;
	}
	public void setConserv(float conserv) {
		this.conserv = conserv;
	}
	public boolean isServico() {
		return servico;
	}
	public void setServico(boolean servico) {
		this.servico = servico;
	}
	public boolean isDisponivel() {
		return disponivel;
	}
	public void setDisponivel(boolean disponivel) {
		this.disponivel = disponivel;
	}
	public String getDatalibera() {
		return datalibera;
	}
	public void setDatalibera(String datalibera) {
		this.datalibera = datalibera;
	}
}
